const DICTIONNARY_LENGTH = DICTIONNARY.length

export default class Token {

  static generate (length = 1024) {
    if (isNaN(length)) { length = 1024 }

    const tmp = new Array(length)
    for (let i = length - 1; i >= 0; --i) {
      tmp[i] = DICTIONNARY[Math.floor(Math.random()*DICTIONNARY_LENGTH)]
    }
    
    return tmp.join('')
  }
}
